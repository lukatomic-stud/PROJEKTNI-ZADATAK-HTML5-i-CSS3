    <section class="article-text">
        <p>
            Odabir prvih penjačkih cipela je važan jer će ti one jako utjecati na udobnost, tehniku i sigurnost.
            Dobre početničke cipele trebaju biti dovoljno udobne da možeš trenirati dulje, ali i dovoljno precizne da učiš pravilno koristiti stopala.
        </p>

        <p>
            Najveća dilema je udobnost naspram performansi. Udobnije cipele obično imaju ravniji kalup i manje su “agresivne”,
            što je odličan izbor za početnike jer omogućuje duže penjanje bez boli i bolju koncentraciju na tehniku.
        </p>

        <p>
            Veličina je ključna: cipela mora biti čvrsta i stabilna, ali ne smije uzrokovati jaku bol ili trnce.
            Kao početnik, izbjegavaj ekstremno smanjivanje broja – cilj ti je da noga ne “pliva”, ali da možeš normalno stajati i hodati kratko do smjera.
        </p>

        <p>
            Obrati pažnju i na kalup (fit): neki modeli su uži, neki širi, neki više odgovaraju “grčkom” tipu stopala,
            a neki “egipatskom”. Najbolje je probati više modela i izabrati onaj u kojem ti prsti prirodno sjednu, bez stvaranja pritiska na jednom mjestu.
        </p>

        <p>
            Za početak su često najbolji modeli s nešto debljim potplatom i čvršćom konstrukcijom jer daju podršku pri učenju stajanja na rubu.
            Mekše i agresivnije cipele imaju smisla kasnije, kad razviješ snagu stopala i preciznost, posebno za bouldering i prevjese.
        </p>

        <p>
            Ako biraš između čička i vezica, čičak je praktičan za brže obuvanje i skidanje, dok vezice omogućuju preciznije stezanje.
            Bez obzira na izbor, važno je da peta dobro sjedi i da nema praznog prostora koji bi smanjio kontrolu na petnim hvatištima.
        </p>

