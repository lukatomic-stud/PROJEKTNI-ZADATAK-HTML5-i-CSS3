    <section class="article-text">
        <p>
            Prvi izlazak na stijenu može biti uzbudljiv, ali i pomalo stresan ako nemaš dobru pripremu.
            Zato je najbolje krenuti s jednostavnim sportskim smjerovima i jasnim planom, bez žurbe i bez velikih očekivanja.
        </p>

        <p>
            Za početak biraj rute koje su kraće, dobro opremljene i s jasnim linijama hvatišta.
            Idealno je penjati na poznatom penjalištu gdje su pristup, osiguravališta i spuštanje jednostavni, a stijena “čitljiva”.
        </p>

        <p>
            Sigurnost je najvažnija stvar na izletu. Prije penjanja uvijek napravi partner check:
            provjeri je li čvor ispravno vezan, je li pojas pravilno stegnut, je li spravica dobro postavljena i je li karabiner zaključan.
        </p>

        <p>
            Od opreme ti trebaju penjačke cipele, pojas, kaciga (preporučeno), spravica za osiguravanje i odgovarajući karabiner,
            te dovoljno quickdrawova za smjer koji planiraš penjati. Ne zaboravi i magnezij, vodu, sunčane naočale te slojevitu odjeću
            jer se uvjeti na stijeni mogu brzo promijeniti.
        </p>

        <p>
            Što se taktike tiče, pokušaj penjati “mirno”: koristi noge koliko god možeš, drži kukove bliže stijeni i radi kratke pauze na dobrim stajalištima.
            Ako zapneš, spusti se malo, odmori i promijeni redoslijed pokreta — početnički smjerovi su idealni za učenje čitanja sekvence.
        </p>

        <p>
            Najbolji savjet za prvi izlet je ići s iskusnijim partnerom ili u organiziranoj grupi.
            Tako ćeš brže naučiti osnove, osjećati se sigurnije i puno više uživati u iskustvu penjanja na prirodnoj stijeni.
        </p>

